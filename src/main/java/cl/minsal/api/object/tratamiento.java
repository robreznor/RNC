package cl.minsal.api.object;

public class tratamiento {
	
	private String participantesComite;
	private String resolucionComite;
	private String descripcionTratamiento;
	private String responsablesTratamiento;
	
	public String getParticipantesComite() {
		return participantesComite;
	}
	public void setParticipantesComite(String participantesComite) {
		this.participantesComite = participantesComite;
	}
	public String getResolucionComite() {
		return resolucionComite;
	}
	public void setResolucionComite(String resolucionComite) {
		this.resolucionComite = resolucionComite;
	}
	public String getDescripcionTratamiento() {
		return descripcionTratamiento;
	}
	public void setDescripcionTratamiento(String descripcionTratamiento) {
		this.descripcionTratamiento = descripcionTratamiento;
	}
	public String getResponsablesTratamiento() {
		return responsablesTratamiento;
	}
	public void setResponsablesTratamiento(String responsablesTratamiento) {
		this.responsablesTratamiento = responsablesTratamiento;
	}

}
